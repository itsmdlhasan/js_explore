// vary + able = variable

var price = 80;

// একটা ভেরিয়েবল ডিক্লেয়ার করার জন্য ৫ টা জিনিস লাগে। প্রথমত "var" কী-ওয়ার্ড, তারপর "price" ভেরিয়েবল এর নাম, তারপর "=" এসাইনমেন্ট অপারেটর, তারপর "80" ভেরিয়েবল ভ্যালু, তারপর পুরো ভেরিয়েবল স্টেটমেন্ট টি ক্লোজ করার জন্য ";" সেমিকোলন।


